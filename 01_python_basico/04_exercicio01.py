"""
Ler dois números inteiros, executar e mostrar 
o resultado das seguintes operações: 
adição, subtração, multiplicação e divisão
"""

n1 = int(input('Digite um número\n'))
n2 = int(input('Digite outro número\n'))

print('Adição: {}'.format(n1 + n2))
print('Subtração: {}'.format(n1 - n2))
print('Multiplicação: {}'.format(n1 * n2))
print('Divisão: {}'.format(n1 / n2))